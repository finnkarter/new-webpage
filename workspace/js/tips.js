    likeTip(tip) {
        // ÌèâÏ†ê ÏïΩÍ∞Ñ Ï¶ùÍ∞Ä (ÏµúÎåÄ 5.0)
        tip.rating = Math.min(5.0, tip.rating + 0.1);
        tip.rating = Math.round(tip.rating * 10) / 10; // ÏÜåÏàòÏ†ê Ï≤´Ïß∏ÏûêÎ¶¨ÍπåÏßÄ
        
        window.toast?.show('ÌîºÎìúÎ∞± Í∞êÏÇ¨Ìï©ÎãàÎã§! üëç', 'success');
        this.closeModal();
        
        // ÌôîÎ©¥ ÏóÖÎç∞Ïù¥Ìä∏
        setTimeout(() => {
            this.renderTips();
        }, 500);
    }

    createModal(title, content, buttons = []) {
        this.closeModal();
        
        const modal = document.createElement('div');
        modal.className = 'tip-modal';
        
        modal.innerHTML = `
            <div class="tip-modal-content">
                <button class="tip-close" onclick="window.tipsApp.closeModal()">‚úï</button>
                <h3 style="margin-bottom: var(--space-4); color: var(--color-text); padding-right: var(--space-8);">${title}</h3>
                <div>${content}</div>
                <div style="display: flex; gap: var(--space-3); justify-content: flex-end; margin-top: var(--space-6);">
                    ${buttons.map(btn => 
                        `<button class="action-btn ${btn.type}" onclick="${btn.action}">${btn.text}</button>`
                    ).join('')}
                </div>
            </div>
        `;
        
        document.body.appendChild(modal);
        this.currentModal = modal;
        
        Utils.fadeIn(modal);
        
        // ESC ÌÇ§Î°ú Îã´Í∏∞
        Utils.on(modal, 'keydown', (e) => {
            if (e.key === 'Escape') {
                this.closeModal();
            }
        });
        
        // Î∞∞Í≤Ω ÌÅ¥Î¶≠ÏúºÎ°ú Îã´Í∏∞
        Utils.on(modal, 'click', (e) => {
            if (e.target === modal) {
                this.closeModal();
            }
        });
        
        return modal;
    }

    closeModal() {
        if (this.currentModal) {
            Utils.fadeOut(this.currentModal).then(() => {
                if (this.currentModal?.parentNode) {
                    this.currentModal.parentNode.removeChild(this.currentModal);
                }
                this.currentModal = null;
            });
        }
    }

    // ÌåÅ Í≤ÄÏÉâ
    searchTips(query) {
        this.searchQuery = query.toLowerCase();
        this.renderTips();
    }

    // Ïù∏Í∏∞ ÌåÅ Í∞ÄÏ†∏Ïò§Í∏∞
    getPopularTips(limit = 5) {
        return [...this.tips]
            .sort((a, b) => b.views - a.views)
            .slice(0, limit);
    }

    // ÏµúÏã† ÌåÅ Í∞ÄÏ†∏Ïò§Í∏∞
    getRecentTips(limit = 5) {
        return [...this.tips]
            .sort((a, b) => b.id - a.id)
            .slice(0, limit);
    }

    // Ï∂îÏ≤ú ÌåÅ Í∞ÄÏ†∏Ïò§Í∏∞ (ÎÜíÏùÄ ÌèâÏ†ê)
    getRecommendedTips(limit = 5) {
        return [...this.tips]
            .sort((a, b) => b.rating - a.rating)
            .slice(0, limit);
    }
